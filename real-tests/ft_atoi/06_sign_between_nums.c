#include "../framework/libunit.h"

int    sign_between_nums(void)
{
    if (ft_atoi("42-42") == 42 
        && ft_atoi("42+42") == 42
        && ft_atoi("42 42") == 42)
        return (0);
    else 
        return (-1);   
}
